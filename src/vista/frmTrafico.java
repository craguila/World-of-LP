    private void tblJugadorMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblJugadorMouseClicked
        String objetivo=tblJugador.getValueAt(tblJugador.getSelectedRow(), 0).toString();
        
        for (EquipoItem i:Juego.jugador.getInventario().getItems()){
            if (i.getNombre().equals(objetivo)){
                Juego.jugador.getInventario().removeItem(i);
                n.getInventario().addItem(i);
                Juego.jugador.ganarDinero(i.getPrecio());
                n.gastarDinero(i.getPrecio());
                setEstado();
                break;  
            }
        }
    }//GEN-LAST:event_tblJugadorMouseClicked

    private void btnExitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExitActionPerformed
        n.interactuando=false;
        Juego.ventanaInventario.setEstado();
        p.releaseTeclado();
        this.dispose();
    }//GEN-LAST:event_btnExitActionPerformed

    private void tblTraficanteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblTraficanteMouseClicked
        String objetivo=tblTraficante.getValueAt(tblTraficante.getSelectedRow(), 0).toString();
        
        for (EquipoItem i:n.getInventario().getItems()){
            if (i.getNombre().equals(objetivo)){
                if(Juego.jugador.gastarDinero(i.getPrecio())){
                    n.getInventario().removeItem(i);
                    Juego.jugador.getInventario().addItem(i);
                    n.ganarDinero(i.getPrecio());
                }
                setEstado();
                break;  
            }
        }
    }//GEN-LAST:event_tblTraficanteMouseClicked



