    public synchronized void setEstado() {
        //Jugador
        DefaultTableModel mdl1 =(DefaultTableModel)this.tblJugador.getModel();
        mdl1.setRowCount(0);
        for(Item it:p.getInventario().getItems()){
            Object data[]={it.getNombre(),it.getPrecio()};
            mdl1.addRow(data);
        }
        this.lblDineroP.setText(p.getDinero()+" monedas.");
        this.tblJugador.setModel(mdl1);
        //Traficante
        DefaultTableModel mdl2 =(DefaultTableModel)this.tblTraficante.getModel();
        mdl2.setRowCount(0);
        for(Item it:n.getInventario().getItems()){
            Object data[]={it.getNombre(),it.getPrecio()};
            mdl2.addRow(data);
        }
        this.lblDineroT.setText(n.getDinero()+" monedas.");
        this.tblTraficante.setModel(mdl2);        
    }

    
